class String_endswith {
    public static void main(String args[]) {
        String s1 = "IT SGGS JAVA PROGRAMMING";
        System.out.println(s1.endsWith("G")); // true
        System.out.println(s1.endsWith("MMING")); // true
        System.out.println(s1.endsWith("JAVA"));// false
    }
}
